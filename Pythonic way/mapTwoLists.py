words = ["one", "two", "three", "four", "five"]
nums = [1, 2, 3, 4, 5]
table = dict(zip(words, nums))

print(f"List 1: {words}")
print(f"List 2: {nums}")
print(f"Table: {table}")

# Result
# List 1: ['one', 'two', 'three', 'four', 'five']
# List 2: [1, 2, 3, 4, 5]
# Table: {'one': 1, 'two': 2, 'three': 3, 'four': 4, 'five': 5}
